package org.com;

public class SwapNUm {

	public static void main(String[] args) {
		int a=10,b=15,c;
		System.out.println("Before swapping");
System.out.println(a);
System.out.println(b);
c=a; //10
a=b; //15
b=c; // 10
System.out.println("After swapping");
System.out.println(a);
System.out.println(b);

	}

}
